<p>
<strong class="bigtext" >Qu'est ce qu'une application Desktop ?</strong><br><br>

Une application dite desktop est une application destinée à l'usage des ordinateurs de bureau ou même les ordinateurs portables. Les applications ont pour but de répondre à des besoins d'automatisation de traitement des informations. L'un des exemples les plus concrets est l'application Word de Microsoft qui permet de rédiger un document sans l'aide d'un papier et d'un stylo. Les applications desktop sont beaucoup utilisées en entreprise, car elle permet t'automatiser leurs services tout en ayant le contrôle en interne des données. Une application desktop peut être native, c'est-à-dire installer directement sur l'ordinateur nécessitant des mises à jour, ou une application web, installée sur un serveur local ou distant, ne nécessitant aucune mise à jour.
</p>


