<strong class="bigtext">Louer votre voiture électrique via une app</strong><br><br>

Carrie est un projet à la fois professionnel et personnel. Je me pose énormément de question sur l'avenir de nos véhicules. Nous vivons une époque difficile avec notamment la flambée des prix du pétrole, il est évident que nous sommes proches de la fin des véhicules à essences ou à gazole. Selon l'AIE, l'Agence Internationale de l’Énergie prévoit une pénurie de pétrole pour 2025. En 2024, aucune voiture à diesel n'aura le droit de circuler dans la zone du grand Paris et en 2030, cette interdiction s'ajoutera aux voitures à essences. Cette réflexion sur le déclin du carburant m'est arrivée le jour de mes vacances dans mon pays natal qui lui aussi touché par une grave pénurie où l'on pouvait voir des longues queues de voiture sur 1km devant des stations services. Il est de ce fait nécessaire de commencer à passer aux voitures électriques. Cependant, ce type de véhicule coûte plus cher qu'une voiture à essences et n'est pas encore démocratie. Afin de démocratie ce véhicule du futur, j'ai pensé qu'il serait intéressant d'avoir une application qui permettrait de louer des voitures électriques dans des parcs automobiles partout en Île-de-France. C'est un projet ambitieux sur le long terme nécessitant énormément de fonctionnalité et de données.
