<p>
<strong class="bigtext" >Qu'est ce que l'UX/UI Design ?</strong><br>

L'UX (User Experience) Design signifie conception pour l'expérience utilisateur, il s'agit d'une disciple fondée par Don Norman ayant pour but de faciliter au maximum les usages d'une interface. De ce fait, l'UX Design doit se mettre dans la peau de l'utilisateur, bien le définir et bien le comprendre. Il est indispensable de faire votre UX avant de vous lancer dans votre production, vous pouvez créer application innovante ou avoir le plus beau site web du monde, si votre UX est mauvaise personne ne voudra l'utiliser. Exemple Windows 8 qui a été un OS plus pensées pour les utilisateurs de tablettes hors la majorité des utilisateurs Windows sont sur PC, de plus l'interface métro a désorienté les utilisateurs habitués de Windows. En plus de nombreux bug récurrent, cet échec commercial est dû à une mauvaise UX, qu'ils ont pu améliorer avec Windows 10 et 11. Une bonne interface est très importante pour une bonne UX, c'est là qu'intervient l'UI Design en complément de l'UX, le but de l'UI Design est de concevoir un prototype de l'interface via des logiciels de design. Le but de l'UI Design est d'avoir une maquette claire qui va être reproduite lors de l'intégration et le développement web.

</p>


</div>


<div class="flexible marger-top">

      <div>
    <strong class="bigtext" >Comment fait-on de l'UX/UI Design ?</strong><br>

    L'UX se fait en avant tout par des analyses pointilleuses de vie et du quotidien de l'utilisateur :<br>
    <ul>
        <li><strong>1-Persona :</strong>  Il s'agit de personnifier les utilisateurs types, faire une description réaliste de ce dernier
        <li><strong>2-Empathy Map :</strong> Se mettre à sa place, savoir ce qu'il ressent, ce qu'il entend, ce qu'il voit, ce qu'il veut faire ainsi que les risques et les opportunités
        <li><strong>3-User Journey :</strong> Plus connu sous le nom du parcours utilisateurs, le fait de définir le processus avant l'utilisation de l'interface jusqu'à l'utilisation finale (ex : commander à manger)
        <li><strong>4-Emotional Design :</strong> Définir les animations et interactions pour toucher l'émotion de l'utilisateur à fin de le guider.
        <li><strong>5-UX Writing :</strong> Définir comment guider l'utilisateur avec des mots
        <li><strong>6-BenchMark :</strong> Analyser les forces et les faibles des interfaces de vos concurrents
        <li><strong>7-Atomic Design:</strong> Une méthode récente et efficace qui consiste à définir le design des éléments de votre UI d'un petit élément (bouton) au plus gros (Page entière)
        <li><strong>8-AB Testing</strong> Il est préférable de concevoir deux interfaces différentes pour obtenir des retours pertinents des utilisateurs.
</ul>

<p>
    
Pour concevoir l'interface, on fait d'abord des wireframes, c'est-à-dire des croquis via le logiciel Axure, puis simuler la navigation de maquette, on utilise soit Figma, Sketch (pour Mac uniquement) ou Adobe XD.

</p> 


