    <strong class="bigtext">Comment créer un support de communication papier ?</strong><br><br>

    Tout d'abord, vous devrez choisir le type de support papier avec son format associé :<br>
    <ul>
      <li><strong>Format A0 ou A1 :</strong> Affiche, Posters <br></li>
      <li><strong>Format A2 :</strong> Impression photo, Kakemono <br></li>
      <li><strong>Format A3 :</strong> Brochures<br> </li>
      <li><strong>Format A4 :</strong> Documents administratifs ou professionnels, livre scolaire<br> </li>
      <li><strong>Format A5,A6, A7 :</strong> Revues,Livret, Pochette ou Flyer <br> </li>
</ul>

<p>
Une fois avoir choisi votre format papier, il ne vous reste plus qu'à définir votre cible, le sujet , le contexte ainsi que le message à la fois implicite et explicite de votre support de communication. Les supports de communications sont réalisés via les logiciels Photoshop et InDesign</p>

