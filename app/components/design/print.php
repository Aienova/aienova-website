<p>
<strong class="bigtext">Qu'est ce qu'un support papier ?</strong><br><br>

Un support de communication papier est sans aucun doute les plus vieux outils de communication depuis l'invasion de l'imprimerie par Johannes Gutenberg en 1440. D'abord utilisé par la presse et les journaux, utiliser par les régimes politiques comme arme favorite de propagande, elle est depuis les années 50 un élément incontournables dans notre société de consommation. Il est beaucoup plus facile et impactant de véhiculer un message à travers une affiche avec une photo, un visage, une police et un slogan. Sans oublier le code couleur qui doit être fidèle à votre marque.
</p>


