<strong class="bigtext">Qu'est ce qu'un logo ?</strong><br>

Une question absurde, je l'admets, nous savons tous que le logo est l'image qui identifie une marque, une entreprise ou une organisation. La vraie question est plutôt "Qu'est-ce que définit une image comme un logo ?". Certains me diront, il s'agit d'un pictogramme un peu comme la pomme d'Apple ou une typographie comme le logo de LVMH. Ce qui définit avant tout un logo, c'est l'histoire qui se cache derrière votre logo, votre logo doit mettre tout un storytelling derrière une seule image. Votre logo doit surtout définir votre code couleur. Exemple photos du logo de la Juventus, qui a bien évolué depuis 2017, il s'est modernisé en adoptons un style plus épuré, avec ce simple J accompagné des deux bandes qui utilise deux symboles des clubs. Une façon de casser la structure du traditionnel logo vieux depuis 1905 tout en restant fidèle à ses symboles. Ce qui se traduit par une volonté de rajeunir l'équipe hors Juventus veut dire "jeunesse" en latin. Chaque logo a son histoire.
